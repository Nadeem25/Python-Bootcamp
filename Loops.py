## For Loop
## for i in range(start, end, increament)

for i in range(5):
    print(i)

for i in range(1,6):
    print(i)

for i in range(1, 10, 2):
    print(i)

for i in range (10, 1, -1):
    print(i)

name = "Nadeem Chaudhary"
for char in name:
    print(f"char:{char}")

